SELECT P.PRODUCT_ID
     , P.PRODUCT_NAME
     , SUM(P.PRICE * O.AMOUNT) AS TOTAL_SALES
  FROM FOOD_PRODUCT P
     , FOOD_ORDER O
 WHERE 1=1
   AND O.PRODUCT_ID = P.PRODUCT_ID 
   AND TO_CHAR(O.PRODUCE_DATE,'YYYYMM') = '202205' 
 GROUP BY P.PRODUCT_ID, P.PRODUCT_NAME
 ORDER BY SUM(P.PRICE * O.AMOUNT) DESC, P.PRODUCT_ID ASC

